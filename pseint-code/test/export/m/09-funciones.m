function sin_titulo()
	x=1.5;
	disp(sqrt(x));
	disp(abs(x));
	disp(log(x));
	disp(exp(x));
	disp(sin(x));
	disp(cos(x));
	disp(tan(x));
	disp(asin(x));
	disp(acos(x));
	disp(atan(x));
	disp(round(x));
	disp(floor(x));
	disp((randi(15)-1));
	s='Hola';
	disp(length(s));
	disp(lower(s));
	disp(upper(s));
	disp(s(2:3));
	disp([s,' Mundo']);
	disp(['Mundo ',s]);
	disp(str2num('15.5'));
	disp(num2str(15.5));
end
